#include <stdio.h>
/**
 * more_numbers - jkjjjj
 * Return: kdkdkd
 */
void more_numbers(void)
{
	int ii;
	int ten;
	int vv;

	for (ten = 0; ten < 10; ten++)
	{
		for (ii = 0; ii <= 9; ii++)
		{
			putchar(ii);
		}
		for (vv = 0; vv < 5; vv++)
		{
			putchar('1');
			putchar(vv);
			printf('\n');
		}

	}
}
